using namespace std;

int main()
{
    int a;
    
    cout << "Maukan Nilai : ";
    cin >> a;
    cout << a << " + 3 = ";
    a += 3;
    cout << a << endl;

    cout << "Maukan Nilai : ";
    cin >> a;
    cout << a << " - 3 = ";
    a -= 3;
    cout << a << endl;

    cout << "Maukan Nilai : ";
    cin >> a;
    cout << a << " x 3 = ";
    a *= 3;
    cout << a << endl;

    cout << "Maukan Nilai : ";
    cin >> a;
    cout << a << " : 3 = ";
    a /= 3;
    cout << a << endl;
    
    return 0;
}
